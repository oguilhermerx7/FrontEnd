function cbr1000rr() {
    document.getElementById("corte_cbr1000rr").play();
}

document.querySelector(".tecla_cbr1000rr").onclick = cbr1000rr;
//essa função abaixo é pra para o som//
var audioCbr1000rr = document.getElementById("corte_cbr1000rr");
function toggleCbr1000rr() {
    if (audioCbr1000rr.paused) {
        audioCbr1000rr.play();
    } else {
        audioCbr1000rr.pause();
        audioCbr1000rr.currentTime = 0;
    }
}
document.querySelector(".tecla_cbr1000rr").onclick = toggleCbr1000rr;



